package mapmakingtools.core.helper;

public class ColourCache {
	
	public int blockId, blockMeta, red, green, blue;
	public ColourCache(int blockId, int blockMeta, int red, int green, int blue) {
		this.blockId = blockId;
		this.blockMeta = blockMeta;
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
}
